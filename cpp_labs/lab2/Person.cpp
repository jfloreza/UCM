// Include our person outline
#include "Person.h"

// Default constructor, nothing here...
Person::Person()
{ }

// Constructor with one string
Person::Person(string _name)
{
	name = _name;
}

// Constructor with one string and an int
Person::Person(string _name, int _age)
{
	name = _name;
	age = _age;
}

void Person::print() {
	
	// We can use cout because Person.h includes <iostream>
	cout << name << " is " << age << " years old." << endl;
	
}
