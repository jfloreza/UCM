#ifndef __PERSON_H__
#define __PERSON_H__

#include <iostream>

using namespace std;

class Person
{
	public:
		Person();

		// copy constructor 
		Person(const Person& );
				                
		// overloaded assignment operator
		Person& operator=(const Person& );

		void setName(string);
		void setAge(int);
		void print();

	private:
		string name;
		int age;

};

#endif


